// Função que retorna o dobro de um número
export function dobro(numero) {
    return numero * 2; // Multiplica o número por 2
}

// Função que retorna a soma de dois números
export function soma(a, b) {
    return a + b; // Soma os valores de "a" e "b"
}

// Função que retorna a subtração de dois números
export function subtracao(a, b) {
    return a - b; // Subtrai "b" de "a"
}
