// Exporta uma função que retorna o dobro de um número
export function dobro(numero) {
    return numero * 2; // Multiplica o número por 2 e retorna o resultado
}

// Exporta uma função que soma dois números e retorna o resultado
export function soma(a, b) {
    return a + b; // Retorna a soma de "a" e "b"
}

// Exporta uma função que subtrai dois números e retorna o resultado
export function subtracao(a, b) {
    return a - b; // Retorna o resultado da subtração de "a" por "b"
}